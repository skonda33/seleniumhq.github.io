package telusuko.linkedList;

public class SivaNode {
    private int data;
    private SivaNode next;

    public int getData() {
        return data;
    }

    public void setData(int data) {
        this.data = data;
    }

    public SivaNode getNext() {
        return next;
    }

    public void setNext(SivaNode next) {

        this.next = next;
    }
}
